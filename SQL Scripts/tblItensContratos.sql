CREATE TABLE TBLITENSCONTRATOS
(
  ID_ITEM integer generated by default as identity primary key,
  QTDE_ITEM decimal(18,2),
  PRECO_UNITARIO decimal(18,4),
  PRECO_TOTAL decimal(18,2),
  NUMERO_ITEM integer,
  DATA_ITEM date,
  SALDO_ITEM decimal(18,2),
  QTDE_SALDOITEM decimal(18,2),
  ID_CONTRATO integer REFERENCES TBLCONTRATOS(ID_CONTRATO),
  ID_PRODUTO integer REFERENCES TBLPRODUTOS(ID_PRODUTO),
  ID_DEPARTAMENTO integer REFERENCES TBLDEPARTAMENTOS(ID_DEPARTAMENTO),
  ID_GENERO integer REFERENCES TBLGENEROS(ID_GENERO),
  ID_USUARIO integer REFERENCES TBLUSUARIOS(ID_USUARIO)
);
