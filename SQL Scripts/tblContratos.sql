CREATE TABLE TBLCONTRATOS
(
  ID_CONTRATO integer generated by default as identity primary key,
  CONTRATO_ADITIVO varchar(1),
  ID_MODALIDADE integer REFERENCES TBLMODALIDADES(ID_MODALIDADE),
  NUMERO_CONTRATO varchar(20),
  NUMERO_LICITACAO varchar(20),
  ANO_EXERCICIO varchar(4),
  DATA_INICIO date,
  DATA_FINAL date,
  ARQUIVO_CONTRATO varchar(255),
  HISTORICO_CONTRATO blob sub_type 1,
  VALOR_CONTRATO decimal(18,2),
  SALDO_CONTRATO decimal(18,2),
  ID_ORGAO integer REFERENCES TBLORGAOS(ID_ORGAO),
  ID_FORNECEDOR integer REFERENCES TBLFORNECEDORES(ID_FORNECEDOR),
  ID_GENERO integer REFERENCES TBLGENEROS(ID_GENERO),
  ID_USUARIO integer REFERENCES TBLUSUARIOS(ID_USUARIO),
  CONTRATO_BYTE blob sub_type 0
);