CREATE TABLE TBLFORNECEDORES
(
  ID_FORNECEDOR integer generated by default as identity primary key,
  RAZAO_SOCIAL varchar(100),
  NOME_FANTASIA varchar(100),
  CPF_CNPJ varchar(20),
  ENDERECO_FORNECEDOR varchar(255),
  TEL_FIXO_FORNECEDOR varchar(15),
  TEL_CEL_FORNECEDOR varchar(15),
  ID_BAIRRO integer references TBLBAIRROS(ID_BAIRRO),
  ID_CIDADE integer REFERENCES TBLCIDADES(ID_CIDADE),
  ID_ESTADO integer REFERENCES TBLESTADOS(ID_ESTADO),
  CONTATO varchar(100),
  EMAIL_FORNECEDOR varchar(100)
);