CREATE TABLE TBLBAIXAITENS
(
  ID_BAIXAITEM integer generated by default as identity primary key,
  ID_ITEM integer REFERENCES TBLITENSCONTRATOS(ID_ITEM),
  NUMERO_NOTA decimal(18,2),
  DATA_BAIXAITEM date,
  QTDE_BAIXAITEM decimal(18,2),
  VALOR_BAIXA decimal(18,2),
  QTDESALDO_BAIXAITEM decimal(18,2),
  SALDO_BAIXAITEM decimal(18,2),
  ID_CONTRATO integer REFERENCES TBLCONTRATOS(ID_CONTRATO),
  ID_DEPARTAMENTO integer REFERENCES TBLDEPARTAMENTOS(ID_DEPARTAMENTO),
  ID_PRODUTO integer REFERENCES TBLPRODUTOS(ID_PRODUTO),
  ID_USUARIO integer REFERENCES TBLUSUARIOS(ID_USUARIO)
);