CREATE TABLE TBLPRODUTOS
(
  ID_PRODUTO integer generated by default as identity primary key,
  DESCRICAO varchar(255),
  DETALHE varchar(255),
  ID_UNIDMEDIDA integer REFERENCES TBLUNIDMEDIDAS(ID_UNIDMEDIDA),
  ID_MARCA integer REFERENCES TBLMARCAS(ID_MARCA),
  ID_GENERO integer REFERENCES TBLGENEROS(ID_GENERO)
);