CREATE TABLE TBLORGAOS
(
  ID_ORGAO integer generated by default as identity primary key,
  NOME_ORGAO varchar(100),
  ENDERECO_ORGAO varchar(255),
  TEL_FIXO_ORGAO varchar(15),
  ID_BAIRRO integer REFERENCES TBLBAIRROS(ID_BAIRRO),
  ID_CIDADE integer REFERENCES TBLCIDADES(ID_CIDADE),
  ID_ESTADO integer REFERENCES TBLESTADOS(ID_ESTADOS),
  ID_RESPONSAVEL integer REFERENCES TBLRESPONSAVEIS(ID_RESPONSAVEL)
);
